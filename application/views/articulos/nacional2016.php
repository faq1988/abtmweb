                    <p>La Asociación Bahiense de Tenis de Mesa organiza el 62° Campeonato Argentino de la disciplina que se desarrollará en la ciudad.</p>

                    <p>El torneo se llevará a cabo del 20 al 23 de julio en el Club Bahiense del Norte y los partidos se jugarán desde las 10 de la mañana. La entrada será libre y gratuita.</p>

                    <p>Marcelo Zualet, presidente de la Asociación, detalló que "contamos con la confirmación de 20 provincias y participarán entre 400 y 500 jugadores, entre los que se encuentran campeones panamericanos y competidores que han participado en los Juegos Olímpicos”.</p>

                    <p>“Este es un torneo de menores y de mayores. Vamos a poder ver en Bahía a los diez mejores jugadores y jugadoras de la Argentina. Es el campeonato más importante de tenis de mesa del país", aseguró el dirigente.</p>

                    <h3>SUBSIDIO MUNICIPAL</h3>

                    <p>El intendente Héctor Gay, el diputado provincial Santiago Nardelli y el subsecretario de Deportes Bernardo Stortoni, entregaron un subsidio a la Asociación Bahiense de Tenis de Mesa, que será destinado a la organización del Campeonato.</p>

                    <p>Recibieron la contribución el propio Zualet, y los dirigentes Gerardo Gutierrez y David Bortnik.</p>

                    <p>Gay aseguró que “hablamos con ellos hace 45 días y nos explicaron la importancia de este Campeonato y de este deporte en particular. Este tipo de organizaciones es a fuerza de remarla y es un gusto ayudarlos”.</p>

                    <p>El subsidio otorgado consiste en un cheque por el monto de 20 mil pesos.</p>
                    
