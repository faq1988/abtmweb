<p>Excelente jornada tenimesística se vivió el sábado pasado en el magnífico Polideportivo Norte, agradecimiento especial a Bahía Basket y la Subsecretaría de Deportes de Bahía Blanca que cada vez lo están dejando mas lindo al salón. Se disputó la segunda fecha del circuito ABTM. Con mas de 120 jugadores, se vivió una nueva jornada a puro Tenis de Mesa en Bahía Blanca, nuevamente destacamos la presencia de la delegación de Viedma con Richard Iturra y Mariano Richmond a la cabeza, quienes están realizando un excelente trabajo con los chicos, también agradecemos la presencia de todos los jugadores de la región que viajaron para estar presente en la segunda fecha y por supuesto a todos nuestros jugadores locales. Jorge Arp (B. Blanca) en Principiantes, Silvio Damiani (Medanos) en 4ta, Isabella Fragapane (Punta Alta) en 3ra, Franco Bucca (B. Blanca) en 1ra y 2da, finalmente Nicolas Fragapane (Punta Alta) en Superdivision fueron los campeones de la jornada. Felicitaciones a todos y nos volveremos a ver el 14 de mayo en la 3ra fecha del circuito puntable ABTM.
