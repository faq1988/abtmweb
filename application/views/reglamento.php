<h3 class="section-heading">REGLAS DE JUEGO:</h3>
<p>Se aplicarán las Reglas de Juego de la ITTF/ULTM/FATM.</p>
 
<h3 class="section-heading">Premios y trofeos:</h3>
<p>En cada torneo que se dispute para principiantes mayores, habrá medallas hasta el 3er puesto. Para las categorías: Principiantes, Superdivision, 1ra, 2da, 3ra y 4ta habrá medallas hasta el 3er puesto. Adicionalmente, las categorías puntables cuentan con trofeos anuales en función de la acumulación de puntos durante los torneos puntables. La entrega de los mismos se realiza en un almuerzo/cena organizado por la ABTM sobre fin de año abierto a todos sus jugadores y familiares.</p>
 
<h3 class="section-heading">Doble participación:</h3>
<p>La doble participación es libre en las categorías 4ta, 3ra, 2da y 1ra. Todo jugador de estas categorías podrá jugar su categoría y la inmediata superior. Por ejemplo un jugador de 2da podrá jugar también en 1ra. Jugadores principiantes deberán optar por jugar en su categoría o 4ta, ya que comienzan a la misma hora. En Superdivision solo tendrán esta posibilidad los 2 primeros de la categoría primera que salgan en el torneo anterior, siempre y cuando no se supere el cupo de jugadores en Superdivision que será de 12 jugadores.</p>
 
<h3 class="section-heading">Sobre los Jugadores:</h3>
<p>Todos los jugadores inscriptos en un torneo tienen la obligación de oficiar de contador de tantos toda vez que la planilla del grupo del que forman parte lo indique durante la fase clasificatoria y, en caso de ser llamado, 2 instancias más luego de quedar eliminado. El jugador que se negara a hacerlo cuando se le solicitara, o se retirara del evento sin autorización de la mesa de control antes de haber cumplido con esta función, podrá ser inhabilitado para participar del próximo torneo del circuito. Esto lo decidirá el tribunal de disciplina. Ir al REGLAMENTO TRIBUNAL DE DISCIPLINA DEPORTIVA ABTM
El jugador que oficia como contador de tantos no podrá resolver ninguna disputa en el transcurso de un partido dando inmediata intervención al Juez General para resolver cualquier duda reglamentaria o desacuerdo entre los contrincantes.
Con el sólo hecho de inscribirse en un torneo el jugador reconoce la autoridad del juez general del mismo y se obliga a acatar las decisiones tomadas por éste durante el evento.
Un jugador que no se presentara a disputar ninguno de los partidos de su zona, no podrá jugar tampoco los partidos de la llave final.
Cada Jugador deberá asistir con su propia paleta al torneo. La misma deberá tener gomas de color rojo por un lado y negro por el otro. No pueden utilizarse paletas de plástico, con gomas de artesanías o corcho. Tampoco se pueden utilizar paletas con inscripciones o dibujos en sus gomas, o sin gomas.
A partir de 4ta categoría las gomas deberán ser aprobadas por ITTF. Listado de Gomas Aprobadas ITTF
Los jugadores podrán recibir consejo en el transcurso de un encuentro únicamente durante los descansos entre set y set y/o en el tiempo muerto solicitado por cualquiera de las partes.</p>
 
<h3 class="section-heading">Normas de convivencia:</h3>
<p>Los jueces generales, directores de liga y jugadores deberán colaborar para mantener en los torneos un clima familiar, ameno y respetuoso.
Se podrá pasar música a bajo volumen mientras no se estén disputando partidos.
No se permitirá fumar dentro del salón de juego.
No se permitirá el consumo de bebidas alcohólicas dentro del salón de juego.
Si cualquier persona jugador, entrenador, árbitro o público no cumple con estas normas, o molesta u ofende a cualquier jugador durante un encuentro podrá ser expulsada del salón de juego por el juez general.</p>
 
<h3 class="section-heading">Nueva disposicion legal:</h3>
<p>Debido a exigencias legales vigentes, al momento de la inscripción, cada jugador deberá firmar un formulario donde declara estar apto para practicar esta actividad física, eximiendo a la ABTM (Asociación Bahiense de Tenis de Mesa) de cualquier responsabilidad en caso de lesión o perjuicio alguno que pueda sufrir en los torneos y/o actividades organizados por la misma.- En el caso de los menores de 18 años, el formulario deberá ser firmado por un mayor a cargo del mismo.-</p>
                   

                
