<h3 class="section-heading">Tipo de torneo para las categorías puntables:</h3>
<p>Los puntables están compuestos de seis (6) fechas a lo largo del año. En cada fecha, el jugador va acumulando puntos y quien logre acumular la mayor cantidad de puntos en 5 de los 6 torneos puntables, será el campeón de su categoría. Al final del año habrá un campeón de 1ª categoría, uno de 2ª, uno de 3ª y así sucesivamente.</p>
 
<h3 class="section-heading">Tipos de torneos principiantes:</h3>
<p>La categoría principiantes tiene un sistema diferente. Sus torneos no son puntables (es decir que no hay acumulación de puntos a lo largo del año) y no participan del Challenger de fin de año. Los principiantes solamente juegan entre sí, y en cada torneo que disputan, los mejores ascienden automáticamente a la 4ª categoría. La cantidad de jugadores que ascienden queda a criterio de la organización.</p>
 
<h3 class="section-heading">Torneo Challenger:</h3>
<p>Se juega a fin de año es con handicap, y pueden jugar todos los jugadores de categorías puntables.</p>
 
<h3 class="section-heading">Sistema de juego para Principiantes, 1ª, 2ª, 3ª y 4ª (Torneos Puntables):</h3>
<p>El sistema de juego consiste en una zona de 3 y/o 4 jugadores de una misma categoría tomados por sorteo (se toma en cuenta los cabeza de serie que se sacan del RANKING ABTM en las categorías puntables, en principiantes será por sorteo). En las zonas de 3 jugadores pasaran los 3 y en las zonas de 4 jugadores pasaran los mejores 3 a la instancia siguiente que será una llave de eliminación simple.</p>
 
