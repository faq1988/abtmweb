<h3 class="section-heading">Cantidad de set a jugar en las diferentes categorías y en las diferentes instancias:</h3>
<p>- Principiantes: Todos los partidos de las zonas y las llaves a 3 set, excepto la final a 5 set.
- 4ta: Todos los partidos de las zonas y las llaves a 3 set, excepto la final a 5 set.
- 3ra y 2da: Todos los partidos de las zonas y las llaves hasta 8vos de final inclusive a 3 set, a partir de 4tos a 5 set.
- 1ra: Todos los partidos de las zonas a 3 set y los partidos de la llave final a partir de 8vos a 5 set.
- SUPERDIVISION: Todos los partidos a 5 set.
Nota: La cantidad de set a jugar podrá ser modificado en sucesivos torneos si la organización considera necesario.</p>
 
