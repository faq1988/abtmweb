                   <h3 class="section-heading"> Categorías:</h3>
<p>Las categorías disponibles serán:
-      Primera
-      Segunda
-      Tercera
-      Cuarta
-      Superdivisión
-      Principiantes Menores
-      Principiantes Mayores
Nota: A excepción de Principiantes el resto de las categorías serán Puntables, donde habrá un ranking y campeones anuales. En los principiantes los ascensos serán por torneo.</p>
 
<h3 class="section-heading">Cantidad de mesas:</h3>
<p>Se habilitaran un total de 12 mesas para la competencia y 4 mesas de peloteo. En ningún caso se podrán usar las mesas de competencias para pelotear.</p>
 
<h3 class="section-heading">Pelotas a utilizar:</h3>
<p>En Superdivision se utilizara Donic Plastic 40+ 3 estrellas blancas, en el resto de las categorías Puntables se usaran Donic Plastic Couch y en Principiantes DHS 1* blancas.
Nota: La organización podrá cambiar esta decisión en torneos sucesivos.</p>
 
 
<h3 class="section-heading">Cierre de inscripción:</h3>
<p>En las categorías 4ta, 3ra, 2da, 1ra y Superdivision la inscripción cerrara el día jueves anterior al torneo a las 20 hs sin excepción. En las categorías Principiantes las inscripciones cerraran 30 minutos antes del inicio de la categoría en el lugar del evento.</p>
 
<h3 class="section-heading">Como Inscribirse a los torneos:</h3>
<p>Se admitirán únicamente inscripciones desde el formulario Web (LINK) o respondiendo al mail de invitación a la siguiente casilla:  contacto.abtm.web@gmail.com</p>
 
