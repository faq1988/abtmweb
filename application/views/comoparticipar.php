                    <h3 class="section-heading">¿Quiénes pueden jugar?</h3>

                    <p>En los torneos pueden participar jugadores desde el nivel inicial (que nunca jugaron un torneo y/o que sólo juegan en su casa o con amigos) hasta jugadores federados. Por supuesto, cada uno en su categoría correspondiente. No importa si sos hombre o mujer o si tenés 6 años o 90 años... Todos pueden participar!</p>

                    <h3 class="section-heading">¿Cómo me inscribo?</h3>

                    <p>El horario de cierre de inscripciones a los torneos para los principiantes como para el resto de las categorías puntables, está especificado en el mail de invitación que les llega previo a cada torneo o en los afiches publicados en la Web y redes sociales. En ningún caso es necesario inscribirse personalmente el día del torneo. Puede hacerse con uno o más días de anticipación por mail a contacto.abtm.web@gmail.com, o via WEB a través del formulario de inscripción.</p>

                    <h3 class="section-heading">¿En qué categoría juego?</h3>

                    <p>Si no estas ranqueado, en el primer torneo en el que participes tenemos que decidir cual es la categoría en la que podés jugar.</p>

                    <h3 class="section-heading">¿Tengo que llevar mi paleta?</h3>

                    <p>Si, es necesario presentarse con su paleta en lo posible, la organización cuenta con cierta cantidad de paletas para alquilar el costo es de $20 y una seña de $100 que se devolverá cuando se entregue la paleta a la organización.
