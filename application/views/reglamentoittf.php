<h3 class="section-heading">2. REGLAS DEL TENIS DE MESA</h3>

2.1 LA MESA 

<p>2.1.1 La superficie superior de la mesa, conocida como superficie de juego, será rectangular, con una longitud de 2,74 m y una anchura de 1,525 m, y estará situada en un plano horizontal a 76 cm del suelo. </p>

<p>2.1.2 La superficie de juego no incluye los laterales de la parte superior de la mesa. </p>

<p>2.1.3 La superficie de juego puede ser de cualquier material y proporcionará un bote uniforme de, aproximadamente, 23 cm al dejar caer sobre ella una pelota reglamentaria desde una altura de 30 cm. </p>

<p>2.1.4 La superficie de juego será de color oscuro, uniforme y mate, con una línea lateral blanca de 2 cm de anchura a lo largo de cada borde de 2,74 m, y una línea de fondo blanca de 2 cm de anchura a lo largo de cada borde de 1,525 m. </p>

<p>2.1.5 La superficie de juego estará dividida en dos campos iguales por una red vertical paralela a las líneas de fondo y será continua en toda el área de cada campo. </p>

<p>2.1.6 Para dobles, cada campo estará dividido en dos medios campos iguales por una línea central blanca de 3 mm de anchura y paralela a las líneas laterales; la línea central será considerada como parte de cada medio campo derecho. </p>

<h3 class="section-heading">2.2 EL CONJUNTO DE LA RED </h3>

<p>2.2.1 El conjunto de la red consistirá en la red, su suspensión y los soportes, incluyendo las fijaciones que los sujetan a la mesa. </p>

<p>2.2.2 La red estará suspendida de una cuerda sujeta en cada uno de sus extremos a un soporte vertical de 15,25 cm de altura; el límite exterior de los soportes estará a 15,25 cm por fuera de las líneas laterales. </p>

<p>2.2.3 La parte superior de la red estará, en toda su longitud, a 15,25 cm sobre la superficie de juego. </p>

<p>2.2.4 La parte inferior de la red deberá estar, en toda su longitud lo más cerca posible de la superficie de juego, y los extremos de la red lo más cerca posible de los soportes. </p>

<h3 class="section-heading">2.3 LA PELOTA </h3>

<p>2.3.1 La pelota será esférica, con un diámetro de 40 mm. </p>

<p>2.3.2 La pelota pesará 2,7 g. </p>

<p>2.3.3 La pelota será de celuloide o de un material plástico similar, blanca o naranja, y mate. </p>

<h3 class="section-heading">2.4 LA RAQUETA </h3>

<p>2.4.1 La raqueta puede ser de cualquier tamaño, forma o peso, pero la hoja deberá ser plana y rígida. </p>

<p>2.4.2 Como mínimo, el 85% del grosor de la hoja será de madera natural; la hoja puede estar reforzada en su interior con una capa adhesiva de un material fibroso tal como fibra de carbono, fibra de vidrio o papel prensado, pero sin sobrepasar el 7,5% del grosor total ó 0,35 mm, siempre la dimensión inferior. </p>

<p>2.4.3 El lado de la hoja usado para golpear la pelota estará cubierto, bien con goma de picos normal con los picos hacia fuera y un grosor total no superior a 2 mm incluido el adhesivo, o bien con goma sándwich con los picos hacia dentro o hacia fuera y un grosor total no superior a 4 mm incluido el adhesivo. </p>

<p>2.4.3.1 La goma de picos normal es una capa sencilla de goma no celular, natural o sintética, con picos distribuidos de manera uniforme por su superficie, con una densidad no inferior a 10 por cm2 ni superior a 50 por cm2. </p>

<p>2.4.3.2 La goma sándwich es una capa sencilla de goma celular, cubierta exteriormente por una capa sencilla de goma de picos normal; el grosor de la goma de picos no debe sobrepasar los 2 mm. </p>

<p>2.4.4 El recubrimiento llegará hasta los límites de la hoja, pero sin sobrepasarlos, si bien la parte más cercana al mango y que se sujeta con los dedos puede quedar al descubierto o cubrirse con cualquier material. </p>

<p>2.4.5 La hoja, cualquier capa en su interior y todo recubrimiento o capa adhesiva en el lado usado para golpear la pelota, serán continuos y de grosor uniforme. </p>

<p>2.4.6 La superficie del recubrimiento de los lados de la hoja, o la de un lado si éste queda sin cubrir, será mate, de color rojo vivo por un lado y negro por el otro. </p>

<p>2.4.7 Pueden permitirse leves alteraciones en la continuidad de la superficie, o en la uniformidad de su color, debidas a roturas accidentales o uso, siempre y cuando las características de la superficie no sufran cambios significativos. </p>

<p>2.4.8 Al comienzo de un partido, y siempre que cambie de raqueta durante el mismo, un jugador mostrará a su contrincante y al árbitro la raqueta que va a usar, y permitirá que la examinen. </p>

<h3 class="section-heading">2.5 DEFINICIONES </h3>

<p>2.5.1 Una jugada es el período durante el cual está en juego la pelota. </p>

<p>2.5.2 La pelota está en juego desde el último momento en que permanece inmóvil en la palma de la mano libre antes de ser puesta en servicio intencionadamente hasta que la jugada se decide como tanto o anulación. </p>

<p>2.5.3 Una anulación es una jugada cuyo resultado no es anotado. </p>

<p>2.5.4 Un tanto es una jugada cuyo resultado es anotado. </p>

<p>2.5.5 La mano de la raqueta es la mano que empuña la raqueta. </p>

<p>2.5.6 La mano libre es la mano que no empuña la raqueta. </p>

<p>2.5.7 Un jugador golpea la pelota si, estando en juego, la toca con su raqueta empuñada con la mano, o con la misma mano por debajo de la muñeca. </p>

<p>2.5.8 Un jugador obstruye la pelota si él, o cualquier cosa que vista o lleve, la toca cuando está por encima de la superficie de juego o se está dirigiendo hacia ésta y no ha sobrepasado su línea de fondo, no habiendo tocado su campo desde la última vez que fue golpeada por su oponente. </p>

<p>2.5.9 El servidor es el jugador que ha de golpear en primer lugar la pelota en una jugada. </p>

<p>2.5.10 El restador es el jugador que ha de golpear en segundo lugar la pelota en una jugada. </p>

<p>2.5.11 El árbitro es la persona designada para dirigir un partido o encuentro. </p>

<p>2.5.12 El árbitro asistente es la persona designada para asistir al árbitro en ciertas decisiones. </p>

<p>2.5.13 Lo que un jugador viste o lleva incluye todo lo que vestía o llevaba al comienzo de la jugada, excluida la pelota. </p>

<p>2.5.14 Se considera que la pelota pasa por encima o alrededor del conjunto de la red si pasa por cualquier lugar que no sea entre la red y los soportes de ésta, o entre la red y la superficie de juego. </p>

<p>2.5.15 Se considera que la línea de fondo se prolonga indefinidamente en ambas direcciones. </p>

<h3 class="section-heading">2.6 SERVICIO CORRECTO </h3>

<p>2.6.1 El servicio comenzará con la pelota descansando libremente sobre la palma abierta e inmóvil de la mano libre del servidor. </p>

<p>2.6.2 Después, el servidor lanzará la pelota hacia arriba lo más verticalmente posible, sin imprimirle efecto, de manera que se eleve al menos 16 cm tras salir de la palma de la mano libre y luego caiga sin tocar nada antes de ser golpeada. </p>

<p>2.6.3 Cuando la pelota está descendiendo, el servidor la golpeará de forma que toque primero su campo y después de pasar por encima o alrededor del conjunto de la red, toque directamente el campo del restador; en dobles, la pelota tocará sucesivamente el medio campo derecho del servidor y del restador. </p>

<p>2.6.4 Desde el comienzo del servicio hasta que es golpeada, la pelota estará por encima del nivel de la superficie de juego y por detrás de la línea de fondo del servidor, y no será escondida al restador por ninguna parte del cuerpo o vestimenta del servidor o su compañero de dobles. </p>

<p>2.6.5 Es responsabilidad del jugador servir de forma que el árbitro o árbitro asistente pueda ver que cumple con los requisitos de un buen servicio. </p>

<p>2.6.5.1 Si no hay árbitro asistente y el árbitro está dudoso sobre la legalidad de un servicio, puede, en la primera ocasión de un partido, advertir al servidor sin conceder un tanto. </p>

<p>2.6.5.2 Si posteriormente en el partido, un servicio del jugador o de su compañero de dobles es de dudosa legalidad, por la misma o cualquier otra razón, el restador ganará un tanto. </p>

<p>2.6.5.3 Siempre que haya un claro incumplimiento de los requisitos de un servicio correcto no se dará advertencia alguna y el restador ganará un tanto. </p>

<p>2.6.6 Excepcionalmente, el árbitro podrá atenuar los requisitos para un servicio correcto cuando él considere que una determinada discapacidad física impide su cumplimiento. </p>

<h3 class="section-heading">2.7 DEVOLUCIÓN CORRECTA </h3>

<p>2.7.1 Tras haber sido servida o devuelta, la pelota será golpeada de forma que pase por encima o alrededor del conjunto de la red y toque el campo del oponente, bien sea directamente o tras haber tocado el conjunto de la red. </p>

<h3 class="section-heading">2.8 ORDEN DE JUEGO </h3>

<p>2.8.1 En individuales, el servidor hará primero un servicio correcto; el restador hará entonces una devolución correcta, y en lo sucesivo, servidor y restador harán devoluciones correctas alternativamente. </p>

<p>2.8.2 En dobles, el servidor hará primero un servicio correcto; el restador hará entonces una devolución correcta; el compañero del servidor hará entonces una devolución correcta; el compañero del restador hará entonces una devolución correcta, y en lo sucesivo, cada jugador, por turno y en este orden, hará una devolución correcta. </p>

<h3 class="section-heading">2.9 ANULACIÓN </h3>

<p>2.9.1 La jugada será anulada: </p>

<p>2.9.1.1 si, en el servicio, la pelota toca el conjunto de la red al pasar por encima o alrededor de ella, siempre y cuando, por lo demás, el servicio sea correcto o si la pelota es obstruida por el restador o su compañero; </p>

<p>2.9.1.2 si se efectúa el servicio cuando el restador o pareja restadora no están preparados, siempre que ni el restador ni su compañero intenten golpear la pelota; </p>

<p>2.9.1.3 si los fallos en el servicio o al hacer una devolución correcta, o cualquier otro cumplimiento de las Reglas, se deben a alteraciones fuera del control del jugador;</p> 

<p>2.9.1.4 si el árbitro o árbitro asistente interrumpen el juego. </p>

<p>2.9.2 Se puede interrumpir el juego: </p>

<p>2.9.2.1 para corregir un error en el orden de servicio, resto o lados; </p>

<p>2.9.2.2 para introducir la regla de aceleración; </p>

<p>2.9.2.3 para amonestar o penalizar a un jugador; </p>

<p>2.9.2.4 cuando las condiciones de juego son perturbadas de forma tal que pudieran afectar al resultado de la jugada. </p>

<h3 class="section-heading">2.10 TANTO </h3>

<p>2.10.1 A menos que la jugada sea anulada, un jugador ganará un tanto: </p>

<p>2.10.1.1 si su oponente no hace un servicio correcto; </p>

<p>2.10.1.2 si su oponente no hace una devolución correcta; </p>

<p>2.10.1.3 si, tras haber realizado un servicio correcto o una devolución correcta, la pelota toca cualquier cosa, excepto el conjunto de la red, antes de ser golpeada por su oponente; </p>

<p>2.10.1.4 si la pelota pasa más allá de su línea de fondo sin haber tocado su campo, tras ser golpeada por su oponente; </p>

<p>2.10.1.5 si su oponente obstruye la pelota; </p>

<p>2.10.1.6 si su oponente golpea la pelota dos veces consecutivas; </p>

<p>2.10.1.7 si su oponente golpea la pelota con un lado de la hoja de la raqueta cuya superficie no cumple los requisitos indicados en los artículos 2.4.3, 2.4.4 y 2.4.5; </p>

<p>2.10.1.8 si su oponente, o cualquier cosa que éste vista o lleve, mueve la superficie de juego; </p>

<p>2.10.1.9 si su oponente, o cualquier cosa que éste vista o lleve, toca el conjunto de la red; </p>

<p>2.10.1.10 si su oponente toca la superficie de juego con la mano libre; </p>

<p>2.10.1.11 si en dobles uno de los oponentes golpea la pelota fuera del orden establecido por el primer servidor y el primer restador; </p>

<p>2.10.1.12 de acuerdo con lo estipulado en la regla de aceleración (2.15.2). </p>

<h3 class="section-heading">2.11 JUEGO </h3>

<p>2.11.1 Ganará un juego el jugador o pareja que primero alcance 11 tantos, excepto cuando ambos jugadores o parejas consigan 10 tantos; en este caso, ganará el juego el primer jugador o pareja que posteriormente obtenga 2 tantos de diferencia. </p>

<h3 class="section-heading">2.12 PARTIDO </h3>

<p>2.12.1 Un partido se disputará al mejor de cualquier número impar de juegos. </p>

<h3 class="section-heading">2.13 ORDEN DEL SERVICIO, RESTO Y LADOS </h3>

<p>2.13.1 El derecho a elegir el orden inicial de servir, restar o lado de la mesa será decidido por sorteo, y el ganador puede elegir servir o restar primero, o empezar en un determinado lado de la mesa. </p>

<p>2.13.2 Cuando un jugador o pareja ha elegido servir o restar primero, o empezar en un lado determinado, el otro jugador o pareja tendrá la otra elección. </p>

<p>2.13.3 Después de cada 2 tantos anotados, el restador o pareja restadora pasará a ser el servidor o pareja servidora, y así hasta el final del juego, a menos que ambos jugadores o parejas hayan anotado 10 tantos o esté en vigor la regla de aceleración. En estos últimos casos, el orden del servicio y del resto será el mismo, pero cada jugador servirá tan sólo un tanto alternativamente. </p>

<p>2.13.4 En cada juego de un partido de dobles, la pareja que tiene el derecho a servir en primer lugar elegirá cuál de los dos jugadores lo hará primero, y en el primer juego de un partido la pareja restadora decidirá cuál de los dos jugadores restará primero; en los siguientes juegos del partido, una vez elegido el primer servidor, el primer restador será el jugador que le servía en el juego anterior. </p>

<p>2.13.5 En dobles, en cada cambio de servicio, el anterior restador pasará a ser servidor, y el compañero del anterior servidor pasará a ser restador. </p>

<p>2.13.6 El jugador o pareja que sirva primero en un juego restará en primer lugar en el siguiente juego del partido. En el último juego posible de un partido de dobles, la pareja restadora cambiará su orden de recepción cuando la primera de las parejas anote 5 tantos. </p>

<p>2.13.7 El jugador o pareja que comienza un juego en un lado de la mesa comenzará el siguiente juego del partido en el otro lado, y en el último juego posible de un partido, los jugadores o parejas cambiarán de lado cuando el primero de los jugadores o parejas anote 5 tantos. </p>

<h3 class="section-heading">2.14 ERRORES EN EL ORDEN DE SERVICIO, RESTO O LADOS </h3>

<p>2.14.1 Si un jugador sirve o resta fuera de su turno, el juego será interrumpido por el árbitro tan pronto como se advierta el error, y se reanudará sirviendo y restando con aquellos jugadores que deberían servir y restar respectivamente según el tanteo alcanzado, de acuerdo con el orden establecido al principio del partido; en dobles, el juego se reanudará con el orden de servicio elegido por la pareja que tenía derecho a servir primero en el juego durante el cual es advertido el error. </p>

<p>2.14.2 Si los jugadores no cambiaron de lado cuando debían haberlo hecho, el juego será interrumpido por el árbitro tan pronto como se advierta el error, y se reanudará con los jugadores en los lados en que deberían estar según el tanteo alcanzado, de acuerdo con el orden establecido al comienzo del partido. </p>

<p>2.14.3 En cualquier caso, todos los tantos conseguidos antes de advertirse un error serán válidos. </p>

<h3 class="section-heading">2.15 REGLA DE ACELERACIÓN </h3>

<p>2.15.1 Excepto que ambos jugadores o parejas hayan llegado al menos a 9 tantos, la regla de aceleración entrará en vigor si un juego no ha finalizado tras 10 minutos de duración, o en cualquier otro momento anterior a petición de ambos jugadores o parejas. </p>

<p>2.15.1.1 Si la pelota está en juego al llegarse al tiempo límite, el juego será interrumpido por el árbitro y se reanudará con el servicio del jugador que sirvió en la jugada que fue interrumpida. </p>

<p>2.15.1.2 Si la pelota no está en juego al llegarse al tiempo límite, el juego se reanudará con el servicio del jugador que restó en la jugada inmediatamente anterior. </p>

<p>2.15.2 A partir de ese momento, cada jugador servirá un tanto por turno hasta el final del juego, y el jugador o pareja restadora ganará un tanto si hace 13 devoluciones correctas. </p>

<p>2.15.3 Una vez que haya entrado en vigor, la regla de aceleración se mantendrá hasta el final del partido.</p>




                
